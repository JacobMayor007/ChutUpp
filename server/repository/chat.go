package repository

import (
	"log"
	"time"
	database "websocket_server/config"
)

type ChatRepository interface {
	SendMessage(senderID, receiverID, message string) (string, error)
	GetChats(userId string) ([]ChatSummary, error)
}

type ChatDB struct {
	sqlDB *database.PostgreDB
}

func InitChatRepository(db *database.PostgreDB) *ChatDB {
	return &ChatDB{
		sqlDB: db,
	}
}

type ChatSummary struct {
	ChatID          string    `json:"chat_id"`
	LastMessage     string    `json:"last_message"`
	LastSenderEmail string    `json:"last_sender_email"`
	LastSenderId    string    `json:"last_sender_id"`
	RecipientEmail  string    `json:"other_user_email"` // Match your JSON output
	RecipientId     string    `json:"other_user_id"`    // Match your JSON output
	CreatedAt       time.Time `json:"created_at"`
	UpdatedAt       time.Time `json:"updated_at"`
}

func (cd *ChatDB) SendMessage(senderID, receiverID, message string) (string, error) {
	//This is the beginning of the transaction to the database
	tx, err := cd.sqlDB.Db.Begin()
	if err != nil {
		return "", err // Return an error, if occurred
	}

	//tx.Rollback will call at the end of process of SendMessage function.
	//	It reverses the transaction of the database.
	// 	So it will not permanently saved the failed transactions.
	defer tx.Rollback()

	// declaring chatID, and message_id variable
	var chatID string
	var message_id string

	//	Just printing to show there is a sender, receiver, and a message
	log.Printf("Sender Id: %s\nReceiver Id: %s\n Message: %s", senderID, receiverID, message)

	//	Script to search the database if the sender, and receiver is in the database or not
	findQuery := `
        SELECT cp1.chat_id
		FROM chat_participants cp1
		JOIN chat_participants cp2 ON cp1.chat_id = cp2.chat_id
		WHERE cp1.user_id = $1 AND cp2.user_id = $2
		LIMIT 1
    `

	// Executing the the script here, and putting the data to scan
	err = tx.QueryRow(findQuery, senderID, receiverID).Scan(&chatID)

	// if statement for there chat exist, and chat does not exist yet
	if err == nil {
		// 2. CHAT EXISTS: Just update the last message

		im := `INSERT INTO messages 
			(chat_id, user_id, content)
			VALUES
			($1, $2, $3)
			RETURNING message_id
			`

		if err := tx.QueryRow(im, chatID, senderID, message).Scan(&message_id); err != nil {
			return "Please try again. Error occurred in sending message", err
		}

		updateQuery := `
			UPDATE chats 
			SET message_data = $1, user_id = $2
			WHERE chat_id = $3
			`
		if _, err := tx.Exec(updateQuery, message_id, senderID, chatID); err != nil {
			return "Please try again. Error occurred in chat box.", err
		}

	} else {
		// 3. CHAT DOES NOT EXIST: Create everything
		// Note 1: chat_id is auto-generated by UUID default in the table
		// Note 2: message is is also auto generated by UUID default in the table

		createChat := `
			insert into chats
			default values
			returning chat_id
		`

		if err := tx.QueryRow(createChat).Scan(&chatID); err != nil {
			return "Please try again. Error occurred in getting the chat id", err
		}

		icp := `
			insert into chat_participants
			(chat_id, user_id)
			VALUES 
			($1, $2),
			($1, $3)
		`

		if _, err := tx.Exec(icp, chatID, senderID, receiverID); err != nil {
			return "Please try again. Error occurred in executing the chat_participants table", err
		}

		im := `
			insert into messages
			(chat_id, user_id, content)
			values
			($1, $2, $3)
			returning message_id
		`

		if err := tx.QueryRow(im, chatID, senderID, message).Scan(&message_id); err != nil {
			return "", err
		}

		uc := `
			update chats
			set message_data = $1, user_id = $2
			where chat_id = $3
		`

		if _, err := tx.Exec(uc, message_id, senderID, chatID); err != nil {
			return "Please try again. There is an error occured update chat room", err
		}

	}

	err = tx.Commit()
	return chatID, err
}

func (cd *ChatDB) GetChats(userId string) ([]ChatSummary, error) {
	query := `
        SELECT 
            c.chat_id, 
            -- Get message content (Handle NULL if no messages exist)
            COALESCE(m.content, '') AS last_message, 
            
            -- Sender Info
            COALESCE(u_sender.email, '') AS last_sender_email,
            COALESCE(u_sender.user_id, '') AS last_sender_id,
            
            -- Recipient Info
            COALESCE(u_recipient.email, '') AS recipient_email,
            COALESCE(u_recipient.user_id, '') AS recipient_id,
            
            c.created_at,
            
            -- VITAL FIX: Use the Message's creation time as the 'Updated At'
            -- If no message exists, fallback to when the chat was created.
            COALESCE(m.updated_at) AS updated_at

        FROM chats c
        
        -- 1. Find the chats I am in
        JOIN chat_participants cp_me ON c.chat_id = cp_me.chat_id AND cp_me.user_id = $1
        
        -- 2. Find the 'other' person in the chat
        JOIN chat_participants cp_other ON c.chat_id = cp_other.chat_id AND cp_other.user_id != $1
        
        -- 3. Join the MESSAGE table using the UUIDs (message_data -> message_id)
        LEFT JOIN messages m ON c.message_data = m.message_id
        
        -- 4. Get User Details
        LEFT JOIN users u_sender ON c.user_id = u_sender.user_id
        LEFT JOIN users u_recipient ON cp_other.user_id = u_recipient.user_id
        
        -- 5. Sort by the calculated updated_at time
        ORDER BY updated_at DESC;
    `

	rows, err := cd.sqlDB.Db.Query(query, userId)
	if err != nil {
		return nil, err
	}
	defer rows.Close()

	var chats []ChatSummary
	for rows.Next() {
		var s ChatSummary

		// Scan matches the SELECT order above
		if err := rows.Scan(
			&s.ChatID,
			&s.LastMessage,
			&s.LastSenderEmail,
			&s.LastSenderId,
			&s.RecipientEmail,
			&s.RecipientId,
			&s.CreatedAt,
			&s.UpdatedAt, // This now holds m.created_at
		); err != nil {
			return nil, err
		}
		chats = append(chats, s)
	}

	return chats, nil
}
